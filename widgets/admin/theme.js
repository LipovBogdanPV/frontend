const btnFull  = document.getElementById('btn-settings-full');
// — Кнопка: Фіксована ширина (повернутися до поля "Ширина панелі")
const btnFixed = document.getElementById('btn-settings-fixed');
// — Ручка-перетягування
const handle   = document.querySelector('.settings-resize-handle');
const root     = document.getElementById('admin-root');

btnFull?.addEventListener('click', () => {
  // 100vw мінус зовнішні відступи контенту, щоб не прилипало
  const px = (n) => `${Math.max(0, Number(n)||0)}px`;
  const pad = Number(val('th-content-x','24')) * 2;
  // розгортаємо контейнер і картку
  const vars = readActive();
  vars['--content-max']   = '100vw';
  vars['--panel-max-w']   = '100vw';
  vars['--settings-width']= `calc(100vw - ${px(pad)})`;
  applyTheme(vars); writeActive(vars);
});

btnFixed?.addEventListener('click', () => {
  // повертаємося до значень з полів "Макс. ширина панелі" та "Ширина панелі"
  const vars = collectVars();
  applyTheme(vars); writeActive(vars);
});

// Drag to resize
if (handle && root) {
  let startX = 0, startW = 0;
  const onMove = (ev) => {
    const dx = ev.clientX - startX;
    const min = parseFloat(getComputedStyle(root).getPropertyValue('--settings-minw')) || 720;
    const max = parseFloat(getComputedStyle(root).getPropertyValue('--settings-maxw')) || 1600;
    const next = Math.max(min, Math.min(max, startW + dx));
    const vars = readActive();
    vars['--settings-width'] = `${Math.round(next)}px`;
    applyTheme(vars); writeActive(vars);
  };
  const onUp = () => {
    document.removeEventListener('mousemove', onMove);
    document.removeEventListener('mouseup', onUp);
    document.body.style.userSelect = '';
    document.body.style.cursor = '';
  };
  handle.addEventListener('mousedown', (ev) => {
    const cs = getComputedStyle(root);
    startX = ev.clientX;
    startW = parseFloat(cs.getPropertyValue('--settings-width')) || 1100;
    document.addEventListener('mousemove', onMove);
    document.addEventListener('mouseup', onUp);
    document.body.style.userSelect = 'none';
    document.body.style.cursor = 'ew-resize';
  });
}