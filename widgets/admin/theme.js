// CSS із змінними
function toCss(vars) {
  const body = Object.entries(vars)
    .map(([k, v]) => `  ${k}: ${v};`)
    .join('\n');
  return `/* generated by theme.js */\n#admin-root{\n${body}\n}`;
}

// Застосувати тему
function applyTheme(vars) {
  styleTag.textContent = toCss(vars);
}

// Підтягнути активний профіль / списки
const readActive   = () => readJSON(LS_ACTIVE, {});
const writeActive  = (vars) => writeJSON(LS_ACTIVE, vars);
const readProfiles = () => readJSON(LS_PROFILES, {});
const writeProfiles= (map) => writeJSON(LS_PROFILES, map);

// Заповнити форму зі збережених змінних
function fillFormFrom(vars) {
  const set = (id, v) => { const el = safeGet(id); if (el && v != null) el.value = String(v).replace(/px|%/g,''); };

  // Layout
  set('th-content-x',        vars['--content-x']);
  set('th-gap',              vars['--admin-gap']);
  set('th-sidebar',          vars['--sidebar-w']);
  set('th-offset-left',      vars['--offset-left']);
  set('th-maxw',             vars['--panel-max-w']);
  set('th-tablew',           vars['--table-w']);

  // Панель налаштувань
  set('th-panel-mt',         vars['--settings-mt']);
  set('th-panel-mb',         vars['--settings-mb']);
  set('th-footer-h',         vars['--footer-h']);
  set('th-settings-maxw',    vars['--settings-maxw']);
  set('th-settings-w',       vars['--settings-width']);
  set('th-settings-minw',    vars['--settings-minw']);
  set('th-settings-zoom',    vars['--settings-zoom']);
  set('th-settings-gap',     vars['--settings-gap']);
  set('th-settings-pad-x',   vars['--settings-pad-x']);

  // Кольори
  set('th-c-page',           vars['--page-bg']);
  set('th-c-text',           vars['--text']);
  set('th-c-dim',            vars['--muted']);
  set('th-accent',           vars['--accent']);
  set('th-card-bg',          vars['--card-bg']);
  set('th-card-br',          vars['--card-br']);

  // Картки
  set('th-radius',           vars['--card-radius']);
  const sh = safeGet('th-shadow'); if (sh) sh.value = vars['--card-shadow'] || '0 10px 30px rgba(0,0,0,.25)';

  // Кнопки
  set('th-btn-bg',           vars['--btn-bg']);
  set('th-btn-text',         vars['--btn-text']);
  set('th-btn-radius',       vars['--btn-radius']);
  set('th-btn-ghost-bg',     vars['--btn-bg-ghost']);
  set('th-btn-ghost-text',   vars['--btn-text-ghost']);

  // Таблиці
  set('th-table-head',       vars['--table-head-bg']);
  set('th-table-row',        vars['--table-row-hover']);
  set('th-table-border',     vars['--table-border']);
  set('th-table-px',         vars['--table-cell-px']);
  set('th-table-py',         vars['--table-cell-py']);

  // Типографіка
  set('th-base-fz',          vars['--base-fz']);
  set('th-font-weight',      vars['--font-weight']);
  set('th-heading-weight',   vars['--heading-weight']);
  const fs = safeGet('th-font-style'); if (fs) fs.value = vars['--font-style'] || 'normal';
}

// Перезаповнити список профілів
function refreshSavedList() {
  const select = safeGet('th-saved'); if (!select) return;
  const profiles = readProfiles();
  const opts = ['<option value="">— Оберіть тему —</option>']
    .concat(Object.keys(profiles).sort().map(k => `<option value="${k}">${k}</option>`));
  select.innerHTML = opts.join('');
}

// Головна ініціалізація UI
export function initThemeUI() {
  const hasSettings = Boolean(document.querySelector('[data-admin-panel="settings"]'));
  if (!hasSettings) return;

  // 1) Активний профіль або дефолти з форми
  const active = readActive();
  if (active && Object.keys(active).length) {
    applyTheme(active);
    fillFormFrom(active);
  } else {
    const vars = collectVars();
    applyTheme(vars);
    writeActive(vars);
  }

  // 2) Live-оновлення при зміні будь-якого інпута (number, color, select, text)
  const container = document.querySelector('[data-admin-panel="settings"]');
  container?.addEventListener('input', (e) => {
    if (!(e.target instanceof HTMLInputElement) && !(e.target instanceof HTMLSelectElement)) return;
    // Не валимося, навіть якщо якогось поля немає
    try {
      const vars = collectVars();
      applyTheme(vars);
      writeActive(vars);
    } catch (err) {
      console.warn('[theme] collect/apply skipped:', err);
    }
  });

  // 3) Кнопки
  safeGet('th-apply')?.addEventListener('click', () => {
    const vars = collectVars(); applyTheme(vars); writeActive(vars);
  });

  safeGet('th-save')?.addEventListener('click', () => {
    const name = (safeGet('th-name')?.value || '').trim();
    if (!name) { alert('Вкажіть назву теми'); return; }
    const profiles = readProfiles();
    profiles[name] = collectVars();
    writeProfiles(profiles);
    writeActive(profiles[name]);
    refreshSavedList();
    alert('Тему збережено ✅');
  });

  safeGet('th-load')?.addEventListener('click', () => {
    const k = safeGet('th-saved')?.value || '';
    if (!k) return;
    const prof = readProfiles()[k];
    if (!prof) return;
    applyTheme(prof); writeActive(prof); fillFormFrom(prof);
  });

  safeGet('th-delete')?.addEventListener('click', () => {
    const k = safeGet('th-saved')?.value || '';
    if (!k) return;
    const profiles = readProfiles();
    delete profiles[k];
    writeProfiles(profiles);
    refreshSavedList();
  });

  safeGet('th-reset')?.addEventListener('click', () => {
    localStorage.removeItem(LS_ACTIVE);
    const vars = collectVars();
    applyTheme(vars); writeActive(vars);
  });

  refreshSavedList();
}

// Автовиклик (якщо модуль підключений напряму в сторінці)
document.addEventListener('DOMContentLoaded', () => {
  try { initThemeUI(); } catch (e) { console.error('[theme] init failed:', e); }
});


