// Найпростіші утиліти
const $  = (sel, root = document) => root.querySelector(sel);
const $$ = (sel, root = document) => Array.from(root.querySelectorAll(sel));

function safeGet(id) { return document.getElementById(id) || null; }
function val(id, fallback = '') {
  const el = safeGet(id);
  if (!el) return String(fallback);
  return String(el.value ?? fallback);
}
function toPx(n, fallback = '') {
  if (n === '' || n == null) return String(fallback);
  const v = Number(n);
  return Number.isFinite(v) ? `${v}px` : String(n);
}
function toPct(n, fallback = '') {
  if (n === '' || n == null) return String(fallback);
  const v = Number(n);
  return Number.isFinite(v) ? `${v}%` : String(n);
}
function toNum(n, fallback = 0) {
  const v = Number(n);
  return Number.isFinite(v) ? v : fallback;
}

function readJSON(key, fallback) {
  try { return JSON.parse(localStorage.getItem(key) || 'null') ?? fallback; }
  catch { return fallback; }
}
function writeJSON(key, data) {
  localStorage.setItem(key, JSON.stringify(data ?? {}));
}

// Стиль, куди кладемо CSS-змінні
const styleTag = (() => {
  let el = $('#admin-theme-vars');
  if (!el) {
    el = document.createElement('style');
    el.id = 'admin-theme-vars';
    document.head.appendChild(el);
  }
  return el;
})();

